int edec_kgen1(void)
{
	u8 buf[0x100];
	int result;

	result = encdec_init();
	if (result != 0)
		return result;

	result = gelic_xmit_data(gelic_bcast_mac_addr, 0xBEEF, cmd_data, sizeof(cmd_data));
	if (result != 0)
		return result;

	result = encdec_send_device_command(0x81, cmd_data, sizeof(cmd_data), buf, 0x80);
	if (result != 0)
		return result;

	result = gelic_xmit_data(gelic_bcast_mac_addr, 0xBEEF, buf, 0x80);
	if (result != 0)
		return result;

	beep(BEEP_DOUBLE);

	lv1_panic(1);

	return 0;
}
